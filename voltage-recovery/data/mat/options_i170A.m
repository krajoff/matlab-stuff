function options = options_i170A()
    % Data-file
    name = 'VR_i170A.mat';
    % Frequency [Hz]
    f = 50;
    % Amplitude of the established line voltage [V]
    uSteady = 7410;
    % Temporal discretization [s]
    disTime = 1e-4;
    % RMS value of the stator winding phase current {A}
    ik = 2159*2^-0.5;
    % Basic resistance [Ohm]
    zn = 10500/(3^0.5*5077);
    % Start time [s]
    startTime = 11.672; 
    startNumber = int32(startTime/disTime);
    % Duration time [s]
    durationTime = 8;
    % End time [s]
    endTime = startTime + durationTime; 
    endNumber = int32(endTime/disTime);
    % Point interval 
    pointInt = (disTime*f)^-1;
    % Cutting first sub-transient process
    cutPoints = int32(1*f);
    % Initial filter for raw signal "medfilt1"
    iFilter = 5;    
    % Filter for smoothness
    nFilter = .02;
    % Spline discretization [s]
    splineTime = 1e-3;
    % Type curve for analysis: 1 - spline, 2 - filtered spline
    noCurve = 2;
    % beta nil
    beta0 = [-uSteady*10; -1; -1];
    options = struct('name', name, ...
        'f', f, ...
        'uSteady', uSteady, ...
        'ik', ik, ...
        'zn', zn, ...
        'startTime', startTime, ...
        'startNumber', startNumber, ...
        'durationTime', durationTime, ...
        'disTime', disTime, ...
        'endTime', endTime, ...
        'endNumber', endNumber, ...
        'pointInt', pointInt, ...
        'iFilter', iFilter, ...
        'nFilter', nFilter, ...
        'cutPoints', cutPoints, ...
        'splineTime', splineTime, ...
        'noCurve', noCurve, ...
        'beta0', beta0);
end